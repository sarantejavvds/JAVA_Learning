package _004_Looping_Statements_in_JAVA._1_for_Loop;

public class _Printing_1_to_10_Numbers_using_for_Loop
{

	public static void main(String[] args) 
	{
		int i;
		
		System.out.println("Printing numbers from 1 to 10 : ");
		for(i=1 ; i<=10 ; i++)
		{
			System.out.println(i);
		}
		
		System.out.println("\nEnd of the for loop");

	}

}
