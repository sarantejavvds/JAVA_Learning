package _004_Looping_Statements_in_JAVA._3_while_Loop;

public class _Printing_1_to_10_Numbers_using_while_Loop
{

	public static void main(String[] args)
	{
		int i = 1;
		
		System.out.println("Printing numbers from 1 to 10 : ");
		while(i <= 10)
		{
			System.out.println(i);
			
			i++;
		}

		System.out.println("\nEnd of the while loop");
	}

}
